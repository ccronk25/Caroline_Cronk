    public void swapTurn()
    {
        if(isRedTurn)
        {
            isRedTurn = false;
        }
        else
        {
            isRedTurn = true;
        }
    }
    
    public void capture(boolean isRed)
    {
        if(isRed)
        {
            redPiecesTaken++;
        }
        else
        {
            blackPiecesTaken++;
        }
        updateText();
        if(blackPiecesTaken == 12)
        {
            jLabel4.setText("Player 1 (Red) wins the game!");
        }
        else if(redPiecesTaken == 12)
        {
            jLabel4.setText("Player 2 (Black) wins the game!");
        }    
    }
    
    public void updateText()
    {
        jLabel7.setText(""+redPiecesTaken);
        jLabel10.setText("" + (12-redPiecesTaken));
        jLabel6.setText(""+blackPiecesTaken);
        jLabel11.setText("" + (12-blackPiecesTaken));
    }
    
