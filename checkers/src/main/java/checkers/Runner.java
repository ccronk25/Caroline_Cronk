    public void newGame()
    {
        Square[][] squares = gameBoard.getSquareArray();
        for(Square[] squareRow : squares)
        {
            for(Square currentSquare : squareRow)
            {
                if(currentSquare.isOccupied())
                {
                   currentSquare.removePiece(currentSquare.getPiece());
                } 
            }
        }
        gameBoard.initiatePieces();
        redPiecesTaken = 0;
        blackPiecesTaken = 0;
        updateText();
        jLabel4.setText("Checkers");
        isRedTurn = true;
    }
    
